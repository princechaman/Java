import java.util.Scanner;
class Bitwise
{
    public static void main (String arr[])
    {
        int a;
        a= 4 & 5;
        System.out.println("a value is "+a);
        a= 4 | 5;
        System.out.println("a value is "+a);
        a= 4 ^ 5;
        System.out.println("a value is "+a);
        a= 8 >> 1;
        System.out.println("a value is "+a);
        a= 8 << 1;
        System.out.println("a value is "+a);
    }
}